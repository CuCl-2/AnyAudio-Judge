# Clean: hallucination filter for rubric items.
# Same backbone model as the decomposer (Qwen3-30B-A3B-Instruct-2507).
# Asks the model whether each rubric item is grounded in the caption or
# whether it introduced concepts that the caption never mentioned.
# ----------------------------------------------------------------------------

CLEAN_SYSTEM_PROMPT = """你是一个严格的数据质量审核员。你的任务是判断每个评估问题(question)是否完全基于给定的caption内容，还是包含了caption中未提及的幻觉信息。

## 判断标准
- "keep": 该question所问的内容可以完全从caption中找到对应描述，没有添加额外信息
- "remove": 该question包含了caption中完全未提及的概念、属性、细节或描述

## 重要说明
- 如果question只是对caption内容的合理改述或提问形式转换，应判为keep
- 如果question引入了caption中根本没有的新信息（如caption没提到的乐器、情感、场景等），应判为remove
- 只关注 \"题目\" 字段的内容是否超出caption范围

请严格按以下JSON格式输出，不要输出其他内容:
{"results": [{"id": 0, "judgment": "keep/remove"}, ...]}"""


def build_clean_user_text(caption: str, rubric):
    """Format caption + rubric into the user message for the cleaner."""
    lines = [f"## Caption:\n{caption}", "", "## 待检查的Questions:"]
    for i, item in enumerate(rubric):
        q = item.get("question", item.get("题目", ""))
        b = item.get("basis", item.get("依据", ""))
        lines.append(f"[Q{i}] 题目: {q}")
        lines.append(f"      依据: {b}")
    return "\n".join(lines)


