def build_generate_user_text(rubric):
    """Build the user text listing all rubric items for mode='generate'."""
    lines = [f"请对以下 {len(rubric)} 道判断题逐一作答："]
    for idx, item in enumerate(rubric):
        dim = item.get("dimension", item.get("维度", ""))
        q = item.get("question", item.get("题目", ""))
        if dim:
            lines.append(f"\n{idx}. 【{dim}】{q}")
        else:
            lines.append(f"\n{idx}. {q}")
    return "\n".join(lines)
